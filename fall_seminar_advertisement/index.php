    	<td colspan="2"><hr /><p>Dr. Donald J. Wheeler has been actively planting the seeds of transformation in companies and organizations around the world for well over 25 years.  He has taught over 1000 seminars in 17 countries, ranging from Alaska to South Africa and from Norway to Malaysia.  He has also had students from over 30 countries attend his public seminars which are held in Knoxville, Tennessee.  Along with his acclaimed seminars, he is frequently invited to address meetings of managers and makes many presentations to professional groups.  </p>
