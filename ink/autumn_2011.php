                <h1 class="brightred">Who is it for?</h1>
				<p>It is ideal for people who understand what process behavior charts are, but who also need to see them in action to understand how and why they work. Suitable for anyone from any level of an organization, this presentation will enable you to relate control chart theory to successful practice in an exciting and engaging way.</p>
				<h1 class="brightred">What is covered?</h1>
				<p>The 17 minute DVD takes the user into the heart of a specific manufacturing process. It shows how effective process behavior charts are for maintaining a process and how useful they can become for problem prevention. The actual chart is shown as a live record of both the process and the measures taken to maintain it. The presentation clearly illustrates the improvements in quality that were brought about, resulting in a process that was stable, predictable and well within the customer’s specification.</p>
